from math import *

# Operations

print(4 * (5 + 5))

my_num = -5

print(my_num * 100)

# Convert to a string

print(str(my_num) + " is My favourite number")

# Get absolute value

print(abs(my_num))

# Pow- power

print(pow(my_num, my_num))

# Max - Min

print(max(4, 100))

print(min(10, 8))

# Round - Floor - Ceil

print(round(9.8))

print(floor(9.8))

print(ceil(9.8))
